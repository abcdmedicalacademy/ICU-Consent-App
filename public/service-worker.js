/**
 * ICU Consent App — Service Worker  (Phase 7)
 * ============================================
 * Strategy: Cache-first for app shell, network-first for content updates.
 *
 * Spec compliance (Section 11):
 *   ✓ Caches entire app on first online load
 *   ✓ All 14 modules functional offline
 *   ✓ Content served from cache when offline
 *   ✓ Email export queued if offline, sent when connection restored
 *   ✓ Content updates applied silently between sessions (never mid-session)
 *   ✓ "New content update available" notification at session end
 *   ✓ Never mid-session update
 */

const CACHE_NAME      = "icu-consent-v1.0.0";
const QUEUE_STORE     = "icu-email-queue";

// ─────────────────────────────────────────────────────────────────────────────
// APP SHELL — everything needed to run offline
// These paths are cached on first install.
// In production, this list is auto-generated by the build tool (Vite/CRA).
// ─────────────────────────────────────────────────────────────────────────────
const APP_SHELL = [
  "/",
  "/index.html",
  "/static/js/main.js",
  "/static/css/main.css",
  "/manifest.json",
  // Fonts — cached so Tamil renders offline
  "https://fonts.googleapis.com/css2?family=Noto+Sans+Tamil:wght@400;600;700&display=swap",
];

// ─────────────────────────────────────────────────────────────────────────────
// INSTALL — cache app shell
// ─────────────────────────────────────────────────────────────────────────────
self.addEventListener("install", (event) => {
  console.log("[SW] Installing ICU Consent App v1.0.0");

  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => {
      // Cache app shell — skip failures so partial installs don't block
      return Promise.allSettled(
        APP_SHELL.map((url) =>
          cache.add(url).catch((err) =>
            console.warn(`[SW] Could not cache ${url}:`, err)
          )
        )
      );
    }).then(() => {
      console.log("[SW] App shell cached. Ready for offline use.");
      // Do NOT call skipWaiting() — wait until old session ends
      // This prevents mid-session updates (spec requirement)
    })
  );
});

// ─────────────────────────────────────────────────────────────────────────────
// ACTIVATE — clean old caches, take control
// ─────────────────────────────────────────────────────────────────────────────
self.addEventListener("activate", (event) => {
  console.log("[SW] Activating — cleaning old caches");

  event.waitUntil(
    caches.keys().then((cacheNames) =>
      Promise.all(
        cacheNames
          .filter((name) => name !== CACHE_NAME)
          .map((name) => {
            console.log("[SW] Deleting old cache:", name);
            return caches.delete(name);
          })
      )
    ).then(() => {
      // Take control of all existing clients
      return self.clients.claim();
    }).then(() => {
      // Notify all open clients that a new version is active
      return self.clients.matchAll().then((clients) => {
        clients.forEach((client) => {
          client.postMessage({ type: "SW_ACTIVATED", version: CACHE_NAME });
        });
      });
    })
  );
});

// ─────────────────────────────────────────────────────────────────────────────
// FETCH — cache-first strategy with network fallback
// ─────────────────────────────────────────────────────────────────────────────
self.addEventListener("fetch", (event) => {
  const { request } = event;
  const url = new URL(request.url);

  // ── Skip non-GET requests (POST to /api/generate-pdf etc.) ──────────────
  if (request.method !== "GET") return;

  // ── Skip cross-origin requests that aren't fonts ─────────────────────────
  const isFontRequest = url.hostname === "fonts.googleapis.com" ||
                        url.hostname === "fonts.gstatic.com";
  if (url.origin !== self.location.origin && !isFontRequest) return;

  // ── API requests: network-first, no cache ────────────────────────────────
  if (url.pathname.startsWith("/api/")) {
    event.respondWith(
      fetch(request).catch(() =>
        new Response(
          JSON.stringify({ error: "Offline — API unavailable" }),
          { status: 503, headers: { "Content-Type": "application/json" } }
        )
      )
    );
    return;
  }

  // ── App shell: cache-first, then network ─────────────────────────────────
  event.respondWith(
    caches.match(request).then((cached) => {
      if (cached) {
        // Serve from cache immediately
        // Simultaneously try to refresh cache in background
        fetch(request)
          .then((networkResponse) => {
            if (networkResponse && networkResponse.status === 200) {
              caches.open(CACHE_NAME).then((cache) => {
                cache.put(request, networkResponse);
              });
            }
          })
          .catch(() => {}); // silent — we already served from cache

        return cached;
      }

      // Not in cache — try network
      return fetch(request)
        .then((networkResponse) => {
          if (!networkResponse || networkResponse.status !== 200) {
            return networkResponse;
          }
          // Cache the new resource
          const responseToCache = networkResponse.clone();
          caches.open(CACHE_NAME).then((cache) => {
            cache.put(request, responseToCache);
          });
          return networkResponse;
        })
        .catch(() => {
          // Completely offline and not cached
          // Return offline fallback for navigation requests
          if (request.mode === "navigate") {
            return caches.match("/index.html");
          }
          return new Response("Offline", { status: 503 });
        });
    })
  );
});

// ─────────────────────────────────────────────────────────────────────────────
// BACKGROUND SYNC — email queue
// When a user tries to email a document while offline, the request is queued
// in IndexedDB and replayed automatically when connection is restored.
// ─────────────────────────────────────────────────────────────────────────────
self.addEventListener("sync", (event) => {
  if (event.tag === "email-queue-sync") {
    console.log("[SW] Background sync: processing email queue");
    event.waitUntil(processEmailQueue());
  }
});

async function processEmailQueue() {
  // Open IndexedDB email queue store
  const db = await openQueueDB();
  const items = await getAllQueueItems(db);

  if (items.length === 0) return;

  console.log(`[SW] Processing ${items.length} queued email(s)`);

  for (const item of items) {
    try {
      const response = await fetch("/api/send-email", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(item.payload),
      });

      if (response.ok) {
        await deleteQueueItem(db, item.id);
        console.log("[SW] Queued email sent:", item.id);

        // Notify the app
        const clients = await self.clients.matchAll();
        clients.forEach((client) => {
          client.postMessage({
            type: "EMAIL_QUEUE_SENT",
            itemId: item.id,
            to: item.payload.to,
          });
        });
      }
    } catch (err) {
      console.warn("[SW] Could not send queued email:", err);
      // Will retry next sync
    }
  }
}

// ─────────────────────────────────────────────────────────────────────────────
// MESSAGES — from app to service worker
// ─────────────────────────────────────────────────────────────────────────────
self.addEventListener("message", (event) => {
  const { type, payload } = event.data || {};

  switch (type) {

    // App signals "session ended" — now safe to activate waiting SW
    case "SESSION_ENDED":
      self.skipWaiting();
      console.log("[SW] Session ended — activating new version");
      break;

    // App requests to queue an email for background sync
    case "QUEUE_EMAIL":
      openQueueDB()
        .then((db) => addQueueItem(db, payload))
        .then(() => {
          // Register background sync
          if (self.registration.sync) {
            return self.registration.sync.register("email-queue-sync");
          }
          // Fallback: try immediately
          return processEmailQueue();
        })
        .catch((err) => console.warn("[SW] Could not queue email:", err));
      break;

    // App requests cache status
    case "GET_CACHE_STATUS":
      caches.has(CACHE_NAME).then((exists) => {
        event.source.postMessage({
          type: "CACHE_STATUS",
          cached: exists,
          version: CACHE_NAME,
        });
      });
      break;

    // App requests to force refresh cache
    case "REFRESH_CACHE":
      caches.delete(CACHE_NAME).then(() => {
        return caches.open(CACHE_NAME).then((cache) =>
          Promise.allSettled(APP_SHELL.map((url) => cache.add(url).catch(() => {})))
        );
      }).then(() => {
        event.source.postMessage({ type: "CACHE_REFRESHED" });
      });
      break;
  }
});

// ─────────────────────────────────────────────────────────────────────────────
// INDEXEDDB HELPERS — email queue storage
// ─────────────────────────────────────────────────────────────────────────────
function openQueueDB() {
  return new Promise((resolve, reject) => {
    const request = indexedDB.open("icu-email-queue", 1);
    request.onupgradeneeded = (event) => {
      const db = event.target.result;
      if (!db.objectStoreNames.contains("queue")) {
        const store = db.createObjectStore("queue", {
          keyPath: "id",
          autoIncrement: true,
        });
        store.createIndex("timestamp", "timestamp", { unique: false });
      }
    };
    request.onsuccess = () => resolve(request.result);
    request.onerror  = () => reject(request.error);
  });
}

function addQueueItem(db, payload) {
  return new Promise((resolve, reject) => {
    const tx    = db.transaction("queue", "readwrite");
    const store = tx.objectStore("queue");
    const item  = { payload, timestamp: Date.now(), status: "pending" };
    const req   = store.add(item);
    req.onsuccess = () => resolve(req.result);
    req.onerror   = () => reject(req.error);
  });
}

function getAllQueueItems(db) {
  return new Promise((resolve, reject) => {
    const tx    = db.transaction("queue", "readonly");
    const store = tx.objectStore("queue");
    const req   = store.getAll();
    req.onsuccess = () => resolve(req.result);
    req.onerror   = () => reject(req.error);
  });
}

function deleteQueueItem(db, id) {
  return new Promise((resolve, reject) => {
    const tx    = db.transaction("queue", "readwrite");
    const store = tx.objectStore("queue");
    const req   = store.delete(id);
    req.onsuccess = () => resolve();
    req.onerror   = () => reject(req.error);
  });
}

